/// JavaScript ///

//Contrução de variáveis

var raio, altura, volume;

// ENTRADA
raio = prompt ('Entre com o valor em raio para a lata de óleo: ')
altura = prompt ('Entre com o valor para altura da lata de óleo: ')

/// Calculo de Volume
volume = 3.14159 * raio*raio * altura;

alert ('O VOLUME DA LATA DE ÓLEO É DE: '+ volume) // SAIDA
