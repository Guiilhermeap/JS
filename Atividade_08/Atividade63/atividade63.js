/// JavaScript ///

//Contrução de variáveis

var grausC, grausF;

// ENTRADA
grausC = prompt ('Entre com um Valor para Temperatura em Graus Celsius')

/// Conversão em Fahrenheit

grausF = (9 * grausC + 160 ) / 5;

alert ('SUA TEMPERATURA EM GRAUS CELSIUS: '+ grausC + '°C') // SAIDA
alert ('SUA TEMPERATURA EM GRAUS FAHRENHEIT: '+ grausF + '°F') // SAIDA